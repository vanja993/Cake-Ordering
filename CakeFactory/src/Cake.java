public class Cake{

	private String shape;
	private String size;
	private String tierNumber;
	private String sponge;
	private String cream;
	private String jam;
	private String fruit;
	private String topings;
	private String address;
	private String date;
	
	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getShape() {
		return shape;
	}

	public void setShape(String shape) {
		this.shape = shape;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getTierNumber() {
		return tierNumber;
	}

	public void setTierNumber(String teirNumber) {
		this.tierNumber = teirNumber;
	}

	public String getSponge() {
		return sponge;
	}

	public void setSponge(String sponge) {
		this.sponge = sponge;
	}

	public String getCream() {
		return cream;
	}

	public void setCream(String cream) {
		this.cream = cream;
	}

	public String getJam() {
		return jam;
	}

	public void setJam(String jam) {
		this.jam = jam;
	}

	public String getFruit() {
		return fruit;
	}

	public void setFruit(String fruit) {
		this.fruit = fruit;
	}

	public String getTopings() {
		return topings;
	}

	public void setTopings(String topings) {
		this.topings = topings;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
}
